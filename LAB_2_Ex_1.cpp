#include <iostream>
using namespace std;

inline double rectArea(double comprimento, double largura) {
    return comprimento * largura;
}

int main() {
    double comprimento, largura;

    cout << "Digite o comprimento do retângulo: ";
    cin >> comprimento;
    
    cout << "Digite a largura do retângulo: ";
    cin >> largura;

    double area = rectArea(comprimento, largura);
    
    cout << "A área do retângulo é: " << area << endl;
    
    return 0;
}
